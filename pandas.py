import numpy as np

# Create a NumPy array
arr = np.array([10, 20, 30, 40, 50])

# Perform some array operations
print("Original array:", arr)
print("Array + 5:", arr + 5)              # Add 5 to each element
print("Array squared:", arr ** 2)         # Square each element
print("Sum of array:", np.sum(arr))       # Sum of all elements
print("Mean of array:", np.mean(arr))     # Mean of the array
